#!/bin/bash

# Название файла, куда будут сохраняться логи
LOG_FILE="backend_logs.txt"

echo "🔍 [1/2] Подключение к серверу и сбор логов..."

# Подключаемся по SSH, берем последние 1000 строк из контейнера бэкенда (и ошибки, и обычные логи)
# и перезаписываем ими локальный файл
ssh root@5.42.127.159 "docker logs --tail 1000 smmbox_backend 2>&1" > "$LOG_FILE"

echo "✅ [2/2] Готово! Логи сохранены в файл: $LOG_FILE"
echo "Теперь открой $LOG_FILE, найди момент с ошибкой и скинь мне."